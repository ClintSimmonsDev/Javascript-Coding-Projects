var currentQuote = '',
  currentAuthor = '';
 const image = document.createElement('img');

function reload(){
  $(window).on('load', function (e) {
            $('#new-quote').on('click', function (e) {
                $("#title").load(" #title > *");
            });
        });
};

function getPhoto() {
  let stubUrl = 'https://boredhumans.b-cdn.net/faces2/'
  let rando = Math.floor(Math.random() * 500)
  let finalUrl = stubUrl.concat(`${rando}.jpg`)
  image.src = finalUrl;
  document.getElementById('photo').appendChild(image);
}

